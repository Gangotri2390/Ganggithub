package unaryoperators;

public class Prefix {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("a= "+a);
		System.out.println("Process a=" +(++a));
		System.out.println("a= "+a);
		System.out.println("b=" +b);
		System.out.println("Process b=" +(b--));
		System.out.println("b=" +b);
		

	}

}
